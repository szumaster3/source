package core.plugin

/**
 * Represents the plugin types.
 * @author Emperor
 */
enum class PluginType {
    /**
     * Action plugin type.
     */
    ACTION,

    /**
     * Dialogue plugin type.
     */
    DIALOGUE,

    /**
     * Activity plugin type.
     */
    ACTIVITY,

    /**
     * Login plugin type.
     */
    LOGIN,

    /**
     * Logout plugin type.
     */
    LOGOUT,

    /**
     * Quest plugin type.
     */
    QUEST
}