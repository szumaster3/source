package content.global.skill.magic


/**
 * Represents the different methods a player can use to teleport.
 */
enum class TeleportMethod {
    /**
     * Teleport using jewelry (e.g., rings, amulets).
     */
    JEWELRY,

    /**
     * Teleport using a spell.
     */
    SPELL,

    /**
     * Teleport via an NPC (e.g., guide or transport).
     */
    NPC,
}
